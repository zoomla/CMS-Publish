<h2><span>风险提示</span>·“假离婚”弄假成真</h2>
<p> 唐乐律师：
根据我国婚姻法规定，合法的婚姻关系只要办理了离婚登记，即宣告婚姻关系的依法解除，所谓的“假离婚”实际上是不存在的，产生的法律效果与真离婚完全一样。若办理了离婚登记，只要有一方不同意复婚，则婚姻关系不能恢复。
补救措施：
假离婚时签订的离婚协议作出财产分割在一定条件下可撤销。我国婚姻法规定，协议离婚后1年内就财产分割问题反悔，请求更变或撤销分割协议的，法院应受理。审理后，如发现订立分割协议时存在欺诈、胁迫等情形的，财产分割协议可撤销。当然，提出撤销一方需就是否存在胁迫、欺诈，提供相应证据来证实。
